/** AI 프롬프트에 주입할 수사 백과사전 레퍼런스 텍스트 */
export function buildInvestigationPromptSection(): string {
  return `
## XCAPE 수사 프레임워크 (반드시 참고)

### 조합 공식
${INVESTIGATION_FORMULA}

### [A] 범행 동기 레퍼런스
- 교내/사내/단체: ${MOTIVES.교내_사내_단체.join(', ')}
- 가정: ${MOTIVES.가정.join(', ')}
- 그 외: ${MOTIVES.그외.join(', ')}

### [B] 범행 방법 레퍼런스
- 살인/폭행: ${METHODS.살인_폭행.join(', ')}
- 강도/절도: ${METHODS.강도_절도.join(', ')}
- 사기/도박: ${METHODS.사기_도박.join(', ')}
- 그 외: ${METHODS.그외.join(', ')}

### [C] 수사 단서 레퍼런스
- 사건현장: ${CLUES.사건현장.join(', ')}
- 신체: ${CLUES.신체.join(', ')}
- 소지품: ${CLUES.소지품.join(', ')}

### [D] 수사 기법 레퍼런스
- 현장 중심: ${TECHNIQUES.현장_중심_수사.join(', ')}
- 과학 수사: ${TECHNIQUES.과학_수사.join(', ')}
- 심리 수사: ${TECHNIQUES.심리_수사.join(', ')}
`.trim();
}

/** AI 프롬프트에 주입할 퀘스트 분류 레퍼런스 텍스트 */
export function buildQuestTaxonomyPromptSection(): string {
  const catLines = Object.entries(QUEST_CATEGORIES)
    .map(([k, v]) => `- ${k} (${v.description}): ${v.subcategories.join(', ')}`)
    .join('\n');

  const typeLines = PUZZLE_TYPES.map((p) => `- ${p.type}: ${p.description}`).join('\n');

  const prodLines = Object.entries(PRODUCTION_ELEMENTS)
    .map(([k, v]) => `- ${k}: ${v.join(', ')}`)
    .join('\n');

  return `
## XCAPE 퀘스트 분류 체계 (반드시 참고)

### 퍼즐 대분류
${catLines}

### 문제 유형
${typeLines}

### 연출 요소
${prodLines}

### 품질 기준
- 다양성: ${QUEST_QUALITY.다양성}
- 개연성: ${QUEST_QUALITY.개연성}
- 연출성: ${QUEST_QUALITY.연출성}
`.trim();
}
